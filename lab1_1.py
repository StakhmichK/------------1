# Read three numbers from the keyboard
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
c = int(input("Введіть третє число: "))

# Calculate the sum, product and \
# exponent of the first number (first - second)
summa = a + b + c
product = a * b * c
power_result = a ** (b - c)

# Output the results
print("Сума чисел дорівнює: ", summa)
print("Добуток чисел дорівнює: ", product)
print("Піднесення першого числа до степеня: ", power_result)
print("Піднесення першого числа до степеня: ", power_result)